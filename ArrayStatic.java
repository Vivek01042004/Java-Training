public class ArrayStatic{
    public static void main(String [] args){
        int a[]={10,25,68,48,97,34,49,21,10,20,35};
        for(int i=0;i<a.length;i++)
        {
            System.out.print(a[i]+" ");
        }
        System.out.println();
        for(int i:a){
            System.out.print(i+" ");
        }
        }
        
}